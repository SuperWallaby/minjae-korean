/**
 * Turn textbook grammar notation into learner-friendly display text.
 * e.g. "-(으)ㄴ 후에" → "은/ㄴ 후에", "-(아/어)서" → "아서/어서"
 */
export function formatGrammarPatternDisplay(wordName: string): string {
  let s = wordName.trim();
  if (!s) return s;

  const hasNotation = s.startsWith("-") || /[()]/.test(s);
  if (!hasNotation) return s;

  if (s.startsWith("-")) s = s.slice(1);

  const prefixRules: Array<[RegExp, string]> = [
    [/^\(으\)ㄴ\/는/, "은/는"],
    [/^\(으\)ㄴ/, "은/ㄴ"],
    [/^\(으\)ㄹ/, "을/ㄹ"],
    [/^\(으\)려/, "으려/려"],
    [/^\(으\)면서도/, "으면서도/면서도"],
    [/^\(으\)면서/, "으면서/면서"],
    [/^\(으\)면/, "으면/면"],
    [/^\(으\)니까요/, "으니까요/니까요"],
    [/^\(으\)니까/, "으니까/니까"],
    [/^\(으\)니/, "으니/니"],
    [/^\(으\)십시오/, "으십시오/십시오"],
    [/^\(으\)세요/, "으세요/세요"],
    [/^\(으\)시다/, "으시/시"],
    [/^\(으\)ㄴ데요/, "은데요/ㄴ데요"],
    [/^\(으\)ㄴ다/, "는다/ㄴ다"],
    [/^\(으\)ㄹ까요/, "을까요/ㄹ까요"],
    [/^\(으\)ㄹ게요/, "을게요/ㄹ게요"],
    [/^\(으\)ㄹ게/, "을게/ㄹ게"],
    [/^\(으\)ㄹ/, "을/ㄹ"],
    [/^\(으\)려다가/, "으려다가/려다가"],
    [/^\(으\)려다/, "으려다/려다"],
    [/^\(으\)러/, "으러/러"],
    [/^\(으\)/, "으/"],
    [/^\(이\)라는\s+것/, "이라는/라는 것"],
    [/^\(이\)라는/, "이라는/라는"],
    [/^\(이\)라고\s+합니다/, "이라고/라고 합니다"],
    [/^\(이\)라고\s+해요/, "이라고/라고 해요"],
    [/^\(이\)라고/, "이라고/라고"],
    [/^\(이\)/, "이/"],
    [/^\(을\/를\)수록/, "을수록/를수록"],
    [/^\(을\/를\)/, "을/를"],
    [/^\(았\/었\)었던/, "았었던/었었던"],
    [/^\(았\/었\)었/, "았었/었었"],
    [/^\(았\/었\)는지도/, "았는지도/었는지도"],
    [/^\(았\/었\)는지/, "았는지/었는지"],
    [/^\(았\/었\)을/, "았을/었을"],
    [/^\(았\/었\)던/, "았던/었던"],
    [/^\(았\/었\)/, "았/었"],
    [/^\(았\/었다\)가/, "았다가/었다가"],
    [/^\(다\)가/, "다가"],
    [/^\(아\/어\)서는/, "아서는/어서는"],
    [/^\(아\/어\)서/, "아서/어서"],
    [/^\(아\/어\)요/, "아요/어요"],
    [/^\(아\/어\)도/, "아도/어도"],
    [/^\(아\/어\)야겠어요/, "아야겠어요/어야겠어요"],
    [/^\(아\/어\)야겠다/, "아야겠다/어야겠다"],
    [/^\(아\/어\)야지/, "아야지/어야지"],
    [/^\(아\/어\)야죠/, "아야죠/어야죠"],
    [/^\(아\/어\)야\s+해요/, "아야 해요/어야 해요"],
    [/^\(아\/어\)야\s+돼요/, "아야 돼요/어야 돼요"],
    [/^\(아\/어\)야\s+하다/, "아야 하다/어야 하다"],
    [/^\(아\/어\)야\s+되다/, "아야 되다/어야 되다"],
    [/^\(아\/어\)야/, "아야/어야"],
    [/^\(아\/어\)\s+줄/, "아/어 줄"],
    [/^\(아\/어\)\s+주/, "아/어 주"],
    [/^\(아\/어\)\s+보/, "아/어 보"],
    [/^\(아\/어\)\s+버/, "아/어 버"],
    [/^\(아\/어\)\s+놓/, "아/어 놓"],
    [/^\(아\/어\)\s+두/, "아/어 두"],
    [/^\(아\/어\)/, "아/어"],
    [/^\(으\)ㄴ\/는\s+것/, "은/는 것"],
    [/^\(으\)ㄴ\/는\s+거/, "은/는 거"],
    [/^\(으\)ㄴ\/는\s+편/, "은/는 편"],
    [/^\(으\)ㄴ\/는\s+쪽/, "은/는 쪽"],
    [/^\(으\)ㄴ\/는\s+대로/, "은/는 대로"],
    [/^\(으\)ㄴ\/는\s+만큼/, "은/는 만큼"],
    [/^\(으\)ㄴ\/는\s+반면에/, "은/는 반면에"],
    [/^\(으\)ㄴ\/는\s+반면/, "은/는 반면"],
    [/^\(으\)ㄴ\/는데다가/, "은/는데다가/ㄴ/는데다가"],
    [/^\(으\)ㄴ\/는데다/, "은/는데다/ㄴ/는데다"],
    [/^\(으\)ㄴ\/는지라도/, "은/는지라도/ㄴ/는지라도"],
    [/^\(으\)ㄴ\/는지라/, "은/는지라/ㄴ/는지라"],
    [/^\(으\)ㄴ\/는/, "은/는"],
  ];

  for (const [re, repl] of prefixRules) {
    const m = s.match(re);
    if (m) {
      s = repl + s.slice(m[0].length);
      break;
    }
  }

  s = s.replace(/\(([^)]+)\)/g, "$1");

  return s.replace(/\s+/g, " ").trim();
}

export function formatUsageGuideHeadline(wordName: string): string {
  return `When to use ${formatGrammarPatternDisplay(wordName)}`;
}

export function formatUsageGuideTitleEn(wordName: string): string {
  return `How to use ${formatGrammarPatternDisplay(wordName)} in Korean`;
}

/** True when wordName uses textbook hyphen/paren notation. */
export function isGrammarNotationPattern(wordName: string): boolean {
  const s = wordName.trim();
  return s.startsWith("-") || /[()]/.test(s);
}
